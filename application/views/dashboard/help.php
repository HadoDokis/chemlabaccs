      <h4>What is LARS</h4>
        
        Simply put, it is an accident reporting system. We're hoping when this is implemented, it collects accurate data on why accidents happen in a lab environment and from there the collected data would help us avoid future incidents.
        <br>
        
      <h4>Getting Started</h4>

        In order to have access to the LARS, one must register. This maybe done by clicking the 'Register' button on the home page. You will see there two kinds of user accounts:

Administrator Account -- meant for Lab administrators, professors etc. They have the ability to create new sections/classes.
Regular User -- meant for students that are participating in a section/class.
On successfully creating an account, you will receive an email with your login information to LARS.

<h4>Creating a Class or Section</h4>

As an administrator, you will first create section for your students to join. You'll asked to enter a password for every section you create. Sharing this password with students (regular users) allows them to join the section you've created.
<br>
On successfully creating a section, you will receive another email with your new section identifier and the section password, both of which you'll need to share with your students.

<h4>Joining a Section</h4>
As a student user, you will navigate to a page titled “Join a Section”. You will be prompted to provide a Section ID # and a Section Password. These must be provided by your professor or teaching assistant. Click “Join!”. If information is correct, you should see a message notifying that you've successfully joined the section.

<h4>Reporting an Accident</h4>
<p>
As a member of a section, you may report an accident, upload pictures or post comments related to an accident. Once you have added an accident report, the lab admin (whoever created this section) will receive an email as confirmation with details you've entered while adding the report. You may choose to edit this report or add comments via the dashboard under "My Accident Reports".
</p>
<h4>Exporting Report Data to a CSV File</h4>

To export ALL existing accident reports from the database to a .CSV file (ex. Excel), click on the Reporting drop down menu then click on “Export to CSV”.

<h4>Visual Data</h4>

To view data in form of pie charts, bar graphs, and get an overview -- click on the Accidents drop down menu from the navigation bar and then click "Data".

