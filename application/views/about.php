 


<p>The Lab Accident Reporting System (LARS) is a web application whose purpose is to allow users to create accident reports that may occur in various types of laboratories. Whether you are in Chemistry or Physics lab or even a Mechanical Engineering lab, LARS makes it simple to document accidents for the professor and class to view so that these types of accident can be avoided in the future.
    Professors or lab Teaching Assistants have the ability to create their own sections for the students to join who are currently taking their labs. Not only can the users view lab reports from their sections, but LARS also offers a correlated data chart of all lab accidents submitted so that students and teachers alike can assess which accidents are most frequent and plan accordingly.
       
